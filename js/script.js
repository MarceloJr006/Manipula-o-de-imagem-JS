function mostra()
{
    let x = document.getElementById("imagem");
    let y = document.getElementById("botao1")
    if(x.style.display == "none")
    {
        x.style.display = "block";
        y.innerHTML = "Ocultar";
    }
    else
    {
        x.style.display = "none";
        y.innerHTML = "Mostrar";
    }
}

